import { NgModule } from '@angular/core';
import { PreloadAllModules, RouterModule, Routes } from '@angular/router';

const routes: Routes = [
  {
    path: '',
    redirectTo: 'login',
    pathMatch: 'full'
  },
  {
    path: 'home',
    loadChildren: () => import('./home/home.module').then(m => m.HomePageModule)
  },
  {
    path: 'list',
    loadChildren: () => import('./list/list.module').then(m => m.ListPageModule)
  },
  { path: 'inventory-list', loadChildren: './inventory-list/inventory-list.module#InventoryListPageModule' },
  { path: 'inventory-edit/:id', loadChildren: './inventory-edit/inventory-edit.module#InventoryEditPageModule' },
  { path: 'inventory-notifications', loadChildren: './inventory-notifications/inventory-notifications.module#InventoryNotificationsPageModule' },
  { path: 'login', loadChildren: './login/login.module#LoginPageModule' }
];

@NgModule({
  imports: [
    RouterModule.forRoot(routes, { preloadingStrategy: PreloadAllModules })
  ],
  exports: [RouterModule]
})
export class AppRoutingModule {}
